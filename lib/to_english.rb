class ToEnglish
	attr_reader :incoming_text, :outgoing_file, :en_to_braille_dictionary
	
	def initialize(filepaths)
		begin
			@incoming_text = File.read("#{filepaths[0]}")
		rescue Errno::ENOENT => exception
			$stderr.puts "#{filepaths[0]} is not a valid filepath. Please try again"
			exit -1
		end
		@outgoing_file = File.new("#{filepaths[1]}", 'w')
		@en_to_braille_dictionary = {
			'a' => ['10', '00', '00'],
			'b' => ['11', '00', '00'],
			'c' => ['10', '01', '00'],
			'd' => ['10', '01', '10'],
			'e' => ['10', '00', '10'],
			'f' => ['11', '01', '00'],
			'g' => ['11', '01', '10'],
			'h' => ['11', '00', '10'],
			'i' => ['01', '01', '00'],
			'j' => ['01', '01', '10'],
			'k' => ['10', '10', '00'],
			'l' => ['11', '10', '00'],
			'm' => ['10', '11', '00'],
			'n' => ['10', '11', '10'],
			'o' => ['10', '10', '10'],
			'p' => ['11', '11', '00'],
			'q' => ['11', '11', '10'],
			'r' => ['11', '10', '10'],
			's' => ['01', '11', '00'],
			't' => ['01', '11', '10'],
			'u' => ['10', '10', '01'],
			'v' => ['11', '10', '01'],
			'w' => ['01', '01', '11'],
			'x' => ['10', '11', '01'],
			'y' => ['10', '11', '11'],
			'z' => ['10', '10', '11'],
			',' => ['01', '00', '00'],
			';' => ['01', '10', '00'],
			':' => ['01', '00', '10'],
			'.' => ['01', '01', '10'],
			'!' => ['01', '10', '10'],
			'(' => ['01', '11', '10'],
			')' => ['01', '11', '10'],
			'?' => ['01', '11', '00'],
			'"' => ['01', '11', '00'],
			"'" => ['00', '10', '00'],
			'-' => ['00', '11', '00'],
			' ' => ['00', '00', '00']
		}
		puts "Created '#{filepaths[1]}' containing #{incoming_text.length} characters"
	end

end